package JavaBasics.Conditionalstatement.ifelsestatement;

public class quesonifelse {
    public static void main(String arg[]) {
        int A = 1;
        int B = 5;

        if (A>=B) {
            System.out.println("A is the largest of 2");
        }else {
            System.out.println("B is the largest of 2");
        }
    }
}
