package JavaBasics.Variable;
public class datatypes {
    public static void main(String args[]) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        float price = 7;
        System.out.println(price);
        int number = 25;
        System.out.println(number);
        
        
    }
}
