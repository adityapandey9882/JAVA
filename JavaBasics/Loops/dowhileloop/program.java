package JavaBasics.Loops.dowhileloop;

public class program {
    public static void main(String[] args) {
        int num = 10;
        int counter = 0;

        do{
            System.out.println("Hello world");
            counter++;
        } while(num>counter);
        
        System.out.println("Helo world printed 10times successfully");
    }
}
