package JavaBasics.Operators;

public class Logicaloperator {
    public static void main(String args[]) {
        // AND operator (&&)
        System.out.println((5>2) && (8>4));
        // OR operator (||)
        System.out.println((5>3) || (7>8));
        // Logical NOT (!)
        System.out.println(!(5>2));
        System.out.println(!(5>8));
    }
}
