package JavaBasics.Operators;

public class unaryoperator {
    public static void main(String args[]) {
        // pre-increment ++a.
        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);
        // post-increment a++.
        int c = 10;
        int d = c++;
        System.out.println(c);
        System.out.println(d);

    } 
}
